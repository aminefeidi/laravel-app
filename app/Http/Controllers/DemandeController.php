    {
        if (isset($request->search)) {
            $demandes = Demande::where('id', $request->search)->get();
        } else {
            $demandes = Demande::all();
        }

        if (isset($request->type)) {
            $demandes = Demande::where('type', $request->type)->get();
        } else {
            $demandes = Demande::all();
        }

        return view('demandes', [
            'demandes' => $demandes,
            'term' => $request->search ?? '',
        ]);
    }

